package fr.esiag.isies.pds.model.bi.finance;

public class DimDate {
	
	private int idDate;
	private int month;
	private int year;
	
	public DimDate(){}
	
	public int getIdDate() {
		return idDate;
	}
	public void setIdDate(int idDate) {
		this.idDate = idDate;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}

}
